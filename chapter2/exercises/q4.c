// Qestion 4: Write a Program that declares several ints and floats
// Then printf their values. Is there any patterns to their values.

#include <stdio.h>

int main(void)
{
  int poo, bum, weez;
  float pork, beef, mutton;
  printf("poo, bum, weez: %d, %d %d\n", poo, bum, weez );
  printf("pork, beef, mutton: %f, %f, %f\n", pork, beef, mutton);
  return 0;
}
